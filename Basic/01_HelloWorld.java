/**
 * 01_HelloWorld.java
 * First Java Program - Understanding the basics
 */

class HelloWorld {
    
    // Main method - Entry point of Java program
    public static void main(String[] args) {
        
        // Printing to console
        System.out.println("Hello, World!");  // Prints with new line
        System.out.print("Hello ");           // Prints without new line
        System.out.print("Java!\n");          // \n for new line
        
        // Multiple lines
        System.out.println("Line 1");
        System.out.println("Line 2");
        System.out.println("Line 3");
        
        // Printing special characters
        System.out.println("Tab:\tAfter Tab");
        System.out.println("Quote: \"Hello\"");
        System.out.println("Backslash: \\");
        System.out.println("Single Quote: \'");
    }
}
